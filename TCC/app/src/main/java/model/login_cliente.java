package model;

public class login_cliente {
    private int id_logcli;
    private String nome_logcli;
    private String senha_logcli;

    public int getId_logcli() {
        return id_logcli;
    }

    public void setId_logcli(int id_logcli) {
        this.id_logcli = id_logcli;
    }

    public String getNome_logcli() {
        return nome_logcli;
    }

    public void setNome_logcli(String nome_logcli) {
        this.nome_logcli = nome_logcli;
    }

    public String getSenha_logcli() {
        return senha_logcli;
    }

    public void setSenha_logcli(String senha_logcli) {
        this.senha_logcli = senha_logcli;
    }
}
